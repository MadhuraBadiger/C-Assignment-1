//Code for area of triangle//
#include<stdio.h>
int main()
{
  float area,base,height;
  printf("Enter the value of base:");
  scanf("%f",&base);
  printf("\nEnter the value of height :");
  scanf("%f",&height);
  area=0.5*base*height; //formula for area of triangle//
  printf("\nArea of triangle=%f sqcm",area);
}


